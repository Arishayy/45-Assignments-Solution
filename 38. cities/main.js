//creating a function
function describing_city(city, country) {
    if (country === void 0) { country = "Pakistan"; }
    console.log("".concat(city, " is in ").concat(country));
}
//executing the function
describing_city("Karachi");
describing_city("Islamabad");
describing_city("Hyderabad");
//executing the function but with different country and city just for my practice
describing_city("Sharjah", "U.A.E");
